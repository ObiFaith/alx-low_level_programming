/**
 * op_add - sum of a by b
 * @a: first num
 * @b: second num
 *
 * Return: the sum of a by b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - difference of a by b
 * @a: first num
 * @b: second num
 *
 * Return: the difference of a by b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - product of a by b
 * @a: first num
 * @b: second num
 *
 * Return: the product of a by b
 */

int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - result of the division of a by b
 * @a: first num
 * @b: second num
 *
 * Return: the remainder of the division of a by b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder of the division of a by b
 * @a: first num
 * @b: second num
 *
 * Return: the remainder of the division of a by b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
